#include "operations.h"

/**
 *add - adds two integers
 *@a: is an integer
 *@b: is an integer
 *Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 *sub - substracts two integers
 *@a: is an integer
 *@b: is an integer
 *Return: substraction
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 *mul - multiplys two integers
 *@a: is an integer
 *@b: is an integer
 *Return: multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 *div - divides two integers
 *@a: is an integer
 *@b: is an integer
 *Return: division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 *mod - modulus or remainder of division of two integers
 *@a: is an integer
 *@b: is an integer
 *Return: remainder
 */

int mod(int a, int b)
{
	return (a % b);
}
